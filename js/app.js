/* Funcion que nos llama a la api de coordenadas e ip */
async function getData(cb, ip){
    /* Hacemos la llamada */    
    let resultado = await fetch(url + ip);
    let json = await resultado.json();

    /* Llamamos a la funcion de callback*/    
    cb(json);
};

